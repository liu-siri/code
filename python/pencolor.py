# importing turtle package
import turtle
 
# set turtle shape
turtle.shape("turtle")
 
# set the colormode
turtle.colormode(255)
 
# use forward by 100 pixel
# default pen color is  black
turtle.forward(100)
 
# change the pencolor
# pencolor is red
turtle.pencolor("red")
 
# use forward by 100 pixel
# then 90 degrees right
turtle.right(90)
turtle.forward(100)
 
# change the pencolor
# pencolor is blue
turtle.pencolor((41,41,253))
 
# use forward by 100 pixel
# then 90 degrees right
turtle.right(90)
turtle.forward(100)
 
# change the pencolor
# pencolor is green
turtle.pencolor(41,253,41)
 
# use forward by 100 pixel
# then 90 degrees right
turtle.right(90)
turtle.forward(100)
 
